#ifndef INC_FND_H_           // 중복 포함 방지를 위한 헤더 가드 시작
#define INC_FND_H_           // 이 파일이 여러 번 include되어도 한 번만 컴파일되도록 함
//================================================================
#include "main.h"            // STM32 HAL 라이브러리 및 기본 설정 포함 (GPIO 제어를 위해 필요)
//================================================================
// 🔹 7세그먼트 각 세그먼트를 표현하기 위한 구조체 정의
typedef struct
{
    GPIO_TypeDef* port;      // 세그먼트가 연결된 포트 (예: GPIOA, GPIOB, GPIOC)
    uint16_t pin;            // 세그먼트가 연결된 핀 번호 (예: GPIO_PIN_0, GPIO_PIN_5)
} Segment_t;
//================================================================
// 🔹 FND 관련 초기화를 수행하는 함수 선언
//    - GPIO 핀을 출력 모드로 설정하고 초기 상태를 끔(OFF)으로 만듦
void FND_Init(void);
//================================================================
// 🔹 숫자(0~9)를 7세그먼트에 표시하는 함수 선언
//    - 매개변수 num: 표시할 숫자 (0~9 범위)
//    - 내부에서 미리 정의된 FND_Font 테이블을 참조하여 해당 숫자 점등 패턴을 출력
//	 -  예시) FND_DisplayNumber(5);			// 숫자 5 출력
void FND_DisplayNumber(uint8_t num);
//================================================================
// 🔹 모든 세그먼트를 끄는 함수 선언
//    - 모든 세그먼트를 OFF 상태로 만들어 FND 표시를 지움
void FND_Clear(void);
//================================================================
// 커스텀 함수들
void fndInit();
void fndClear();
void fndDisplay1();		// 1켜짐
void fndDisplay2();		// 2켜짐
void fndDisplay3();		// 3켜짐

void fndTest();



#endif /* INC_FND_H_ */       // 헤더 가드 종료
