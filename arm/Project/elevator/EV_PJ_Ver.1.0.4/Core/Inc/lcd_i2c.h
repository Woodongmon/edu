#ifndef INC_LCD_I2C_H_
#define INC_LCD_I2C_H_
//========================================================================
#include "main.h"
#define LCD_I2C_ADDRESS (0x27 << 1)   // LCD의 기본 I2C 주소 (0x27 또는 0x3F인 경우도 있음)
//========================================================================
/*
 * LCD_Init()
 * ----------------
 * LCD 모듈을 초기화합니다.
 * I2C 통신을 설정하고 LCD의 동작 모드(4비트, 2라인, 커서 등)를 설정합니다.
 * 반드시 main 함수 시작 시 한 번 호출해야 합니다.
 * 예시 : LCD_Init(&hi2c1);        // LCD 초기화
 */
void LCD_Init(I2C_HandleTypeDef *hi2c);
//========================================================================
/*
 * LCD_Clear()
 * ----------------
 * LCD 화면을 전체 초기화(클리어)합니다.
 * 모든 문자를 지우고 커서를 (0,0)으로 이동시킵니다.
 * LCD_Clear(&hi2c1);
 */
void LCD_Clear(I2C_HandleTypeDef *hi2c);
//========================================================================
/*
 * LCD_SetCursor()
 * ----------------
 * LCD의 커서를 특정 위치로 이동시킵니다.
 * row: 줄 번호 (0 또는 1)
 * col: 열 번호 (0~15)
 * LCD_SetCursor(&hi2c1, 0, 0);			// 첫 번째 줄에 출력
 * LCD_SetCursor(&hi2c1, 1, 0);			// 두 번째 줄에 출력
 */
void LCD_SetCursor(I2C_HandleTypeDef *hi2c, uint8_t row, uint8_t col);
//========================================================================
/*
 * LCD_Print()
 * ----------------
 * 문자열을 LCD에 출력합니다.
 * 커서 위치부터 문자열이 순서대로 표시됩니다.
 * LCD_Print(&hi2c1, "Current Floor...");
 * 한글은 폰트가 깨짐
 */
void LCD_Print(I2C_HandleTypeDef *hi2c, char *str);
//========================================================================
#endif

